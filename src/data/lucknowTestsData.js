function roundPrice(n) { return Math.ceil(n / 10) * 10; }

function getCategory(name) {
  const n = name.toUpperCase();
  if (/PCR|DNA|RNA|RT-PCR|QUANTITATIVE RT|VIRAL LOAD|GENOTYP/.test(n))      return 'Molecular Tests (PCR)';
  if (/CULTURE|AFB CULT/.test(n))                                             return 'Microbiology';
  if (/AFB|GRAM STAIN|KOH|SMEAR|INDIA INK|FUNGUS STAIN|NAIL SCRAPING/.test(n)) return 'Microbiology';
  if (/CYTOLOGY|FNAC|BIOPSY|PAP SMEAR|ASPIRATION|CURETTING|IHC MARKER/.test(n)) return 'Cytology & Histopathology';
  if (/GLUCOSE|HBA1C|GLYCOSYLATED|FRUCTOSAMINE|INSULIN|C-PEPTIDE|GTT|GCT/.test(n)) return 'Diabetes & Thyroid';
  if (/THYROID|TSH|T3|T4|THYROXIN|TPO|THYROGLOBULIN/.test(n))               return 'Diabetes & Thyroid';
  if (/SGOT|SGPT|ALT|AST|BILIRUBIN|LIVER|LFT|ALKALINE PHOSPHATASE|GAMMA GT|GGT/.test(n)) return 'Liver & Kidney Function';
  if (/CREATININE|UREA|KIDNEY|BUN|GFR|CYSTATIN|RENAL/.test(n))               return 'Liver & Kidney Function';
  if (/CHOLESTEROL|LIPID|TRIGLYCERIDE|HDL|LDL|APOLIPOPROTEIN|TROPONIN|CPK|CRP|BNP|CARDIAC|PROCALCITONIN/.test(n)) return 'Cardiac Health';
  if (/VITAMIN|FOLATE|FOLIC|FERRITIN|IRON|CALCIUM|ZINC|MAGNESIUM|COPPER|PHOSPHORUS|SELENIUM/.test(n)) return 'Vitamins & Minerals';
  if (/TESTOSTERONE|ESTRADIOL|PROGESTERONE|PROLACTIN|FSH|LH|CORTISOL|ACTH|DHEA|ANDROSTENE|AMH|INHIBIN|SHBG|GH|GROWTH HORMONE|RENIN|ALDOSTERONE|CALCITONIN|PARATHYROID|PTH|GASTRIN|ERYTHROPOIETIN/.test(n)) return 'Hormones';
  if (/IgG|IgM|IgA|IgE|ANTIBODY|ANTIGEN|ANA|ANCA|COMPLEMENT|ANTI |TORCH|DENGUE|HEPATITIS|HIV|HERPES|CMV|EBV|RUBELLA|TOXOPLASMA|CHIKUNGUNYA|BRUCELLA|LEPTOSPIRA|WIDAL|TYPHOID|WEIL FELIX|SCRUB|MEASLES|MUMPS|VARICELLA|SYPHILIS|VDRL|RPR|TPHA|HTLV|HPV/.test(n)) return 'Immunology & Serology';
  if (/URINE/.test(n) && !/CORTISOL.*URINE|CATECHOLAMINE|MICRO ALBUMIN|OXALATE/.test(n)) return 'Urine Tests';
  if (/STOOL|MOTION|OCCULT/.test(n))                                          return 'Stool Tests';
  if (/CBC|HAEMOGRAM|HAEMOGLOBIN|RBC|WBC|PLATELET|ESR|BLEEDING|CLOTTING|APTT|PT |D-DIMER|FIBRINOGEN|COOMB|RETICULOCYTE|PCV|MCH|MCV|BLOOD GROUP|RDW/.test(n)) return 'Blood Tests';
  return 'Blood Tests';
}

function getSampleType(name) {
  const n = name.toUpperCase();
  if (/24 HR.*URINE|SPOT URINE|URINE/.test(n))     return 'Urine';
  if (/CSF|CEREBROSPINAL/.test(n))                  return 'CSF';
  if (/STOOL|MOTION|OCCULT/.test(n))                return 'Stool';
  if (/SPUTUM/.test(n))                             return 'Sputum';
  if (/ASCITIC/.test(n))                            return 'Ascitic Fluid';
  if (/PLEURAL/.test(n))                            return 'Pleural Fluid';
  if (/SYNOVIAL/.test(n))                           return 'Synovial Fluid';
  if (/PERICARDIAL/.test(n))                        return 'Pericardial Fluid';
  if (/BIOPSY|TISSUE|CYTOLOGY.*FLUID|FNAC|CURETTING/.test(n)) return 'Tissue/Fluid';
  if (/SEMEN/.test(n))                              return 'Semen';
  if (/SWAB|THROAT|NASAL|EAR|VAGINAL|WOUND|CONJUNCTIVAL|URETHRAL/.test(n)) return 'Swab';
  if (/NAIL|SCRAPING/.test(n))                      return 'Nail';
  return 'Blood';
}

function isFasting(name) {
  const n = name.toUpperCase();
  return /FASTING|LIPID|GLUCOSE.*FASTING|GTT|GCT|TRIGLYCERIDE|CHOLESTEROL|INSULIN.*F/.test(n);
}

function isHomeCollection(name) {
  const n = name.toUpperCase();
  return !/CULTURE|BIOPSY|FNAC|CYTOLOGY|SPUTUM|SWAB|MANTOUX|TUBERCULIN|PAP SMEAR|KARYOTYPING|NAIL SCRAPING/.test(n);
}

